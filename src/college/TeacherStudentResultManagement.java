    private void btninsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btninsertActionPerformed
        // TODO add your handling code here:
        String sem = cmbsem.getSelectedItem().toString();
        String branch = cmbbra.getSelectedItem().toString();
        String sub = subcode.getSelectedItem().toString();
        String tname = teachname.getText();
        String tpass = teachpasscode.getText();
        String permission = cmbper.getSelectedItem().toString();
        
        if(sem.equals("Select") || branch.equals("Select") || sub.equals("Select") || permission.equals("Select"))
        {
            JOptionPane.showMessageDialog(null, "Select the Subject or Semester or Branch or Permission");
        }
        else if(tname.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Please Enter the Name");
        }
        else if(tpass.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Please Enter the Passcode");
        }
        else
        {
            try{
                query = null;
                rowcount = 0;
                query = "insert into TeacherSubPermission values('" + tpass + "','" + tname + "'," + sem + ",'" + branch + "','" + sub + "','" + permission + "')";
                rowcount = DatabaseQuery.ExecuteUpdate(query);
                JOptionPane.showMessageDialog(null, "Permission Given to " + tname + " for Subject " + sub);
            
                
            }
            catch(Exception e)
            {
                e.printStackTrace();
            }
            finally
            {
                ClearForm();
                FillTable();
            }
        }
        
        
    }//GEN-LAST:event_btninsertActionPerformed

